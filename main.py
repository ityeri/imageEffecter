def circle(effecsurObject: effecsur, effectData: dict, effectParameter: any):
    mainEffectData = effectData['data']

    if effectData['main'] == 0:
        #초기 원 사이즈를 화면 크기에 비례하여 무작위적으로 설정
        mainEffectData['size'] = randint(int(effectParameter['screenSize'] * 0.075), int(effectParameter['screenSize'] * 0.25))

    elif effectData['main'] == 1:
        for i in range(50):
            x = randint(0, effecsurObject.originalSurface.get_width() - 1)
            y = randint(0, effecsurObject.originalSurface.get_height() - 1)
            color = effecsurObject.originalSurface.get_at((x, y))

            offset = effecsurObject.surfaceOffset

            pygame.draw.circle(effecsurObject.mainSurface, color, (x + offset[0], y + offset[1]), mainEffectData['size'])
            # pygame.draw.circle(effecsurObject.mainSurface, (0, 0, 0), (x + offset[0], y + offset[1]), mainEffectData['size'], 1)

        if mainEffectData['size'] > 2:
            mainEffectData['size'] *= 0.99

def line(effecsurObject: effecsur, effectData: dict, effectParameter: any) -> None:
    mainEffectData = effectData['data']

    if effectData['main'] == 0:
        mainEffectData['width'] = clamp(randint(int(effectParameter['screenSize'] * 0.005), int(effectParameter['screenSize'] * 0.007)), 1)
        mainEffectData['length'] = randint(int(effectParameter['screenSize'] * 0.1), int(effectParameter['screenSize'] * 0.3))

    elif effectData['main'] == 1:
        for i in range(100):
            x = randint(0, effecsurObject.originalSurface.get_width() - 1)
            y = randint(0, effecsurObject.originalSurface.get_height() - 1)
            color = effecsurObject.originalSurface.get_at((x, y))

            offset = effecsurObject.surfaceOffset

            pygame.draw.line(effecsurObject.mainSurface, color, 
                (x - mainEffectData['length'] + offset[0], y - mainEffectData['length'] + offset[1]),
                (x + mainEffectData['length'] + offset[0], y + mainEffectData['length'] + offset[1]), 
                mainEffectData['width'])

        if mainEffectData['length'] > 1:
            mainEffectData['length'] *= 0.99

def polygon(effecsurObject: effecsur, effectData: dict, effectParameter: any) -> None:
    mainEffectData = effectData['data']

    if effectData['main'] == 0:
        mainEffectData['pointCount'] = randint(3, 6)
        mainEffectData['radius'] = randint(int(effectParameter['screenSize'] * 0.1), int(effectParameter['screenSize'] * 0.3))

    elif effectData['main'] == 1:
        for i in range(100):
            x = randint(0, effecsurObject.originalSurface.get_width() - 1)
            y = randint(0, effecsurObject.originalSurface.get_height() - 1)
            color = effecsurObject.originalSurface.get_at((x, y))

            offset = effecsurObject.surfaceOffset
            pointCount = mainEffectData['pointCount']

            points = []
            radius = mainEffectData['radius']

            for i in range(pointCount):
                rx = x + randint(round(-radius/2), round(radius/2))
                ry = y + randint(round(-radius/2), round(radius/2))

                points.append((rx + offset[0], ry + offset[1]))
            
            pygame.draw.polygon(effecsurObject.mainSurface, color, points)
            # pygame.draw.polygon(effecsurObject.mainSurface, (0, 0, 0), points, 1)


        if mainEffectData['radius'] > 5:
            mainEffectData['radius'] *= 0.99


